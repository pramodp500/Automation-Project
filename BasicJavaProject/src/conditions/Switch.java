package conditions;

public class Switch {

	public static void main(String[] args) {
		int a = 40;
		switch(a)
		{
			case 10 : System.out.println("my no. is 10");
			break;
			case 20 : System.out.println("my no. is 20");
			break;
			case 30 : System.out.println("my no. is 30");
			break;
			default : System.out.println("my no. is not");
		
		}
	}

}
